__author__ = "Xavi"

class Calculadora():
    def __init__(self):
        super.__init__()
    
    def suma(a, b):
        return a+b
    
    def resta(a, b):
        return a-b
    
    def multiplica(a, b):
        return a*b
    
    def divisio(a, b):
        return a/b